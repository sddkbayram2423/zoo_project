package tools;

/**
 * 
 * EntityManager.class  creat location for entitees by using this class 
 * @author Sýddýk Bayram
 */
public class EntitiyLocation {
	
	private int x,y;

	public EntitiyLocation(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	

}
